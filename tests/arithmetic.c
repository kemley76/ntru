int test_lift() {
    uint8_t *bytes1 =
        malloc(PACKED_S3_BYTES); // original hex values come from S3
    hex_to_bytes(hex1, PACKED_S3_BYTES, bytes1);

    poly p1 = {0}, result = {0};
    unpack_S3(bytes1, &p1);
    Sq(&p1);
    Lift(&p1, &result);
    Sq(&result);

    uint8_t *packed = malloc(PACKED_SQ_BYTES);
    pack_Sq(&result, packed);

    char *hex_output = malloc(PACKED_SQ_BYTES * 2 + 1);
    bytes_to_hex(packed, PACKED_SQ_BYTES, hex_output);

    if (strncmp(expected_lift, hex_output, PACKED_SQ_BYTES * 2)) {
        printf("test_lift: lifted polynomial does not match\n%s\n!=%s\n",
               expected_lift, hex_output);
        return 0;
    }

    printf("test_lift: test passed\n");
    return 1;
}

